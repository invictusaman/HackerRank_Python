def print_rangoli(size):
    # your code goes here
    alphabets = "abcdefghijklmnopqrstuvwxyz"
    output = []
    width = 4 * size - 3
    
    for i in range(size):
        str1 = '-'.join(alphabets[i:size])
        row = (str1[::-1] + str1[1:]).center(width, '-')
        output.append(row)
    
    # Print top half 
    for line in output[::-1]:
        print(line)

    # Print bottom half (excluding middle)
    for line in output[1:]:
        print(line)
    
if __name__ == '__main__':
    n = int(input())
    print_rangoli(n)
